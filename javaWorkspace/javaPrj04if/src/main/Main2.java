package main;

public class Main2 {

	public static void main(String[] args) {
		
		System.out.println("=====switch 문=====");
		
		//switch ~ case, break
		int x = 10;
		switch(x) {
		case 10: System.out.println("x 는 10 입니다."); break;
		case 20: System.out.println("x 는 20 입니다."); break;
		case 30: System.out.println("x 는 30 입니다."); break;
		}
		
		
	}//main

}//class








