package codeup;

import java.util.Scanner;

public class Problem1074 {

	public static void main(String[] args) {
		
		//정수 입력받고, 카운트다운
		
		Scanner sc = new Scanner(System.in);
		
		int n = sc.nextInt();
		
		for(int i = n ; i >= 1; --i) {
			System.out.println(i);
		}

	}//main

}//class



























