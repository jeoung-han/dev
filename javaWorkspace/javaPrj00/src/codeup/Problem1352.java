package codeup;

import java.util.Scanner;

public class Problem1352 {

	public static void main(String[] args) {
		
		// n입력받고, 정사각형 모양으로 * 출력
		
		Scanner sc = new Scanner(System.in);
		int n =sc.nextInt();
		
		for(int i = 0 ; i < n; i++) {
			for(int j = 0 ; j < n; j++) {
				System.out.print("*");
			}
			System.out.println();
		}
		
		
		
		

	}//main

}//class






















































